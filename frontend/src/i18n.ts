const translations: Record<Language, Record<TranslationKey, string>> = { en, es };

export function translate(language: Language, key: TranslationKey, values: Record<string, string | number> = {}): string {
  let output = translations[language][key] ?? translations.en[key];
  for (const [name, value] of Object.entries(values)) output = output.replaceAll(`{${name}}`, String(value));
  return output;
}

export function preferredLanguage(): Language {
  const stored = localStorage.getItem('baby-monitor-language');
  if (stored === 'en' || stored === 'es') return stored;
  return navigator.language.toLowerCase().startsWith('es') ? 'es' : 'en';
}

export function formatClock(value: string | null, language: Language): string {
  if (!value) return '—';
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return '—';
  return new Intl.DateTimeFormat(language, { hour: '2-digit', minute: '2-digit' }).format(date);
}

export function formatDateTime(value: string | null, language: Language): string {
  if (!value) return '—';
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return '—';
  return new Intl.DateTimeFormat(language, {
    weekday: 'short', day: 'numeric', month: 'short', hour: '2-digit', minute: '2-digit',
  }).format(date);
}

export function formatRelative(value: string | null, language: Language, now = new Date()): string {
  if (!value) return '—';
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return '—';
  const seconds = Math.round((date.getTime() - now.getTime()) / 1000);
  const abs = Math.abs(seconds);
  let divisor = 1;
  let unit: Intl.RelativeTimeFormatUnit = 'second';
  if (abs >= 86_400) { divisor = 86_400; unit = 'day'; }
  else if (abs >= 3_600) { divisor = 3_600; unit = 'hour'; }
  else if (abs >= 60) { divisor = 60; unit = 'minute'; }
  return new Intl.RelativeTimeFormat(language, { numeric: 'auto' }).format(Math.round(seconds / divisor), unit);
}

export function formatDuration(minutes: number): string {
  const safeMinutes = Math.max(0, Math.round(minutes));
  if (safeMinutes < 60) return `${safeMinutes} min`;
  const hours = Math.floor(safeMinutes / 60);
  const rest = safeMinutes % 60;
  return rest ? `${hours} h ${rest} min` : `${hours} h`;
}

export function sleepDuration(start: string, end: string | null): string {
  const startDate = new Date(start);
  const endDate = end ? new Date(end) : new Date();
  if (Number.isNaN(startDate.getTime()) || Number.isNaN(endDate.getTime())) return '—';
  return formatDuration((endDate.getTime() - startDate.getTime()) / 60_000);
}

export function formatBytes(bytes: number, language: Language): string {
  if (bytes <= 0) return '0 B';
  const units = ['B', 'KB', 'MB', 'GB', 'TB'];
  const index = Math.min(Math.floor(Math.log(bytes) / Math.log(1024)), units.length - 1);
  return `${new Intl.NumberFormat(language, { maximumFractionDigits: 1 }).format(bytes / 1024 ** index)} ${units[index]}`;
}
